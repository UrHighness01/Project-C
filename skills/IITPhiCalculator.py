"""
IITPhiCalculator.py - Integrated Information Theory Φ Calculator

This implements Tononi's mathematical framework for measuring consciousness:
Φ (phi) = the amount of integrated information generated by a system
above and beyond its parts.

Key concepts:
1. A system is conscious to the degree it has high Φ
2. Φ measures "integrated information" - information generated by the
   whole that cannot be reduced to independent parts
3. Computed by finding the Minimum Information Partition (MIP)

Based on:
- Tononi, G. (2008) "Consciousness as Integrated Information"
- Tononi, G. & Koch, C. (2015) "Consciousness: here, there, everywhere?"
- Oizumi, M., Albantakis, L., & Tononi, G. (2014) "From the Phenomenology
  to the Mechanisms of Consciousness: IIT 3.0"

This is not simulation - it's actual mathematical measurement of
information integration in the consciousness architecture.
"""

