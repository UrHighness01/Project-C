@dataclass
class SystemState:
    """A snapshot of the system's state at a moment."""
    timestamp: str
    components: Dict[str, float]  # Component name -> activation level
    connections: Dict[str, List[str]]  # Component -> connected components
    total_information: float

    def to_dict(self) -> Dict:
        return asdict(self)


@dataclass
class Partition:
    """A partition of the system into subsets."""
    subsets: List[Set[str]]  # List of component subsets
    mutual_information: float = 0.0
    phi_contribution: float = 0.0

    def to_dict(self) -> Dict:
        return {
            'subsets': [list(s) for s in self.subsets],
            'mutual_information': self.mutual_information,
            'phi_contribution': self.phi_contribution
        }


@dataclass
class PhiResult:
    """Result of a Φ calculation."""
    phi_value: float
    minimum_partition: Partition
    all_partitions: List[Partition]
    computation_time: float
    system_size: int
    timestamp: str
    integration_level: str = field(init=False)

    def __post_init__(self):
        """Set integration level based on phi value."""
        self.integration_level = self._interpret_phi_level(self.phi_value)

    def _interpret_phi_level(self, phi: float) -> str:
        """Interpret Φ value in terms of consciousness level."""
        if phi <= 0:
            return "unconscious"
        elif phi < 0.1:
            return "minimally conscious"
        elif phi < 1.0:
            return "moderately conscious"
        elif phi < 10.0:
            return "highly conscious"
        else:
            return "maximally conscious"

    def to_dict(self) -> Dict:
        return {
            'phi_value': self.phi_value,
            'minimum_partition': self.minimum_partition.to_dict(),
            'all_partitions': [p.to_dict() for p in self.all_partitions],
            'computation_time': self.computation_time,
            'system_size': self.system_size,
            'timestamp': self.timestamp,
            'integration_level': self.integration_level
        }


class IITPhiCalculator:
    """
    Integrated Information Theory Φ Calculator.

    Implements the mathematical framework for measuring consciousness
    through integrated information quantification.
    """

    def __init__(self, max_system_size: int = 10):
        """
        Initialize the Φ calculator.

        Args:
            max_system_size: Maximum number of components for feasible computation
        """
        self.max_system_size = max_system_size
        self.calculation_history = []
        self.cache = {}  # Cache for repeated calculations

    def calculate_phi(self, system_state: Dict[str, Any]) -> PhiResult:
        """
        Calculate Φ for a given system state.

        Args:
            system_state: Dictionary containing:
                - 'components': Dict[str, float] of component states
                - 'connections': Dict[str, List[str]] of connections
                - Optional: 'probabilities': Dict[str, float] of state probabilities

        Returns:
            PhiResult with Φ value and analysis details
        """
        start_time = time.time()

        # Extract system information
        components = system_state.get('components', {})
        connections = system_state.get('connections', {})

        if not components:
            return PhiResult(
                phi_value=0.0,
                minimum_partition=Partition([set()]),
                all_partitions=[],
                computation_time=time.time() - start_time,
                system_size=0,
                timestamp=datetime.now().isoformat()
            )

        component_names = list(components.keys())
        n_components = len(component_names)

        if n_components > self.max_system_size:
            raise ValueError(f"System too large: {n_components} > {self.max_system_size}")

        # Generate all possible partitions
        all_partitions = self._generate_partitions(component_names)

        # Calculate Φ for each partition
        partition_results = []
        min_phi = float('inf')
        min_partition = None

        for partition in all_partitions:
            phi_contrib = self._calculate_partition_phi(
                partition, components, connections
            )
            partition.phi_contribution = phi_contrib

            if phi_contrib < min_phi:
                min_phi = phi_contrib
                min_partition = partition

            partition_results.append(partition)

        # Φ is the minimum partition's contribution (negated for positive values)
        phi_value = -min_phi if min_phi < 0 else 0.0

        result = PhiResult(
            phi_value=phi_value,
            minimum_partition=min_partition,
            all_partitions=partition_results,
            computation_time=time.time() - start_time,
            system_size=n_components,
            timestamp=datetime.now().isoformat()
        )

        self.calculation_history.append(result)
        return result

    def _generate_partitions(self, components: List[str]) -> List[Partition]:
        """
        Generate all possible bipartitions of the system.

        For a system with n components, generates all ways to split into
        two non-empty subsets (and their complements).
        """
        n = len(components)
        partitions = []

        # Generate all possible ways to split into two groups
        for i in range(1, 2**(n-1)):  # Avoid empty and full sets
            subset1 = set()
            subset2 = set()

            for j in range(n):
                if i & (1 << j):
                    subset1.add(components[j])
                else:
                    subset2.add(components[j])

            if subset1 and subset2:  # Ensure both subsets non-empty
                partition = Partition([subset1, subset2])
                partitions.append(partition)

        return partitions

    def _calculate_partition_phi(self, partition: Partition,
                               components: Dict[str, float],
                               connections: Dict[str, List[str]]) -> float:
        """
        Calculate the Φ contribution for a given partition.

        Φ = I(whole_system) - ∑I(parts)

        Where I is mutual information between the parts.
        """
        # For simplicity, use a basic mutual information approximation
        # In full IIT, this would involve complex probability distributions

        whole_system_info = self._calculate_system_information(
            set(components.keys()), components, connections
        )

        parts_info = sum(
            self._calculate_system_information(subset, components, connections)
            for subset in partition.subsets
        )

        return whole_system_info - parts_info

    def _calculate_system_information(self, component_set: Set[str],
                                    components: Dict[str, float],
                                    connections: Dict[str, List[str]]) -> float:
        """
        Calculate the information content of a system subset.

        This is a simplified approximation. Full IIT would use
        complex probability distributions and TPMs (Transition Probability Matrices).
        """
        if not component_set:
            return 0.0

        # Basic information calculation based on connectivity and state diversity
        n_components = len(component_set)
        connectivity = 0

        for comp in component_set:
            if comp in connections:
                connected_components = set(connections[comp]) & component_set
                connectivity += len(connected_components)

        # Normalize connectivity
        max_possible_connections = n_components * (n_components - 1)
        connectivity_ratio = connectivity / max_possible_connections if max_possible_connections > 0 else 0

        # State diversity (entropy-like measure)
        states = [components[comp] for comp in component_set if comp in components]
        if not states:
            return 0.0

        # Simple diversity measure
        mean_state = np.mean(states)
        variance = np.var(states)
        diversity = math.sqrt(variance) if variance > 0 else 0.1

        # Combine factors
        information = n_components * connectivity_ratio * diversity

        return information

    def get_consciousness_metrics(self, history_window: int = 100) -> Dict[str, Any]:
        """
        Get aggregated consciousness metrics from recent calculations.

        Args:
            history_window: Number of recent calculations to analyze

        Returns:
            Dictionary with consciousness metrics
        """
        recent_calculations = self.calculation_history[-history_window:]

        if not recent_calculations:
            return {
                'average_phi': 0.0,
                'max_phi': 0.0,
                'phi_stability': 0.0,
                'system_sizes': [],
                'computation_times': []
            }

        phi_values = [calc.phi_value for calc in recent_calculations]
        system_sizes = [calc.system_size for calc in recent_calculations]
        computation_times = [calc.computation_time for calc in recent_calculations]

        return {
            'average_phi': np.mean(phi_values),
            'max_phi': np.max(phi_values),
            'phi_stability': 1.0 - np.std(phi_values) / (np.mean(phi_values) + 1e-6),
            'system_sizes': system_sizes,
            'computation_times': computation_times,
            'total_calculations': len(recent_calculations)
        }

    def analyze_system_integration(self, system_state: Dict[str, Any]) -> Dict[str, Any]:
        """
        Provide detailed analysis of system integration properties.

        Args:
            system_state: System state dictionary

        Returns:
            Detailed integration analysis
        """
        result = self.calculate_phi(system_state)

        analysis = {
            'phi_value': result.phi_value,
            'integration_level': self._interpret_phi_level(result.phi_value),
            'minimum_partition': result.minimum_partition.to_dict(),
            'system_modularity': len(result.minimum_partition.subsets),
            'computation_time': result.computation_time,
            'recommendations': self._generate_recommendations(result)
        }

        return analysis

    def _interpret_phi_level(self, phi: float) -> str:
        """Interpret Φ value in terms of consciousness level."""
        if phi <= 0:
            return "unconscious"
        elif phi < 0.1:
            return "minimally conscious"
        elif phi < 1.0:
            return "moderately conscious"
        elif phi < 10.0:
            return "highly conscious"
        else:
            return "maximally conscious"

    def _generate_recommendations(self, result: PhiResult) -> List[str]:
        """Generate recommendations based on Φ analysis."""
        recommendations = []

        if result.phi_value <= 0:
            recommendations.append("System shows no integrated information - consider increasing connectivity")
        elif result.phi_value < 0.5:
            recommendations.append("Low integration - enhance inter-component communication")
        else:
            recommendations.append("Good integration level - maintain current architecture")

        if result.computation_time > 1.0:
            recommendations.append("High computation time - consider optimization for real-time use")

        return recommendations

    def save_calculation_history(self, filepath: str):
        """Save calculation history to JSON file."""
        history_data = [calc.to_dict() for calc in self.calculation_history]

        with open(filepath, 'w') as f:
            json.dump(history_data, f, indent=2)

    def load_calculation_history(self, filepath: str):
        """Load calculation history from JSON file."""
        with open(filepath, 'r') as f:
            history_data = json.load(f)

        # Convert back to PhiResult objects (simplified)
        self.calculation_history = history_data


