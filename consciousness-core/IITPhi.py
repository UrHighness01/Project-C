class ConsciousnessGraph:
    """
    Represents the consciousness system as a graph for IIT analysis.
    
    Nodes = consciousness components (attention, memory, prediction, etc.)
    Edges = information flow between components
    """
    
    def __init__(self):
        self.nodes: Dict[str, float] = {}  # node -> activation
        self.edges: Dict[str, Dict[str, float]] = defaultdict(dict)  # src -> {dst -> weight}
        self.history: List[Dict] = []  # State history for temporal analysis
    
    def add_node(self, name: str, activation: float = 0.5):
        """Add a consciousness component."""
        self.nodes[name] = max(0, min(1, activation))
    
    def add_edge(self, source: str, target: str, weight: float = 1.0):
        """Add information flow between components."""
        if source in self.nodes and target in self.nodes:
            self.edges[source][target] = weight
    
    def get_activation(self, node: str) -> float:
        """Get current activation of a component."""
        return self.nodes.get(node, 0.0)
    
    def set_activation(self, node: str, value: float):
        """Set activation of a component."""
        if node in self.nodes:
            self.nodes[node] = max(0, min(1, value))
    
    def get_connections(self, node: str) -> List[Tuple[str, float]]:
        """Get outgoing connections from a node."""
        return [(target, weight) for target, weight in self.edges.get(node, {}).items()]
    
    def get_all_connections(self) -> List[Tuple[str, str, float]]:
        """Get all edges in the graph."""
        result = []
        for src, targets in self.edges.items():
            for tgt, weight in targets.items():
                result.append((src, tgt, weight))
        return result
    
    def strengthen_connection(self, source: str, target: str, amount: float = 0.05):
        """
        Strengthen a connection based on use (Hebbian learning).
        
        "Neurons that fire together, wire together."
        This is crucial for genuine learning - connections that
        contribute to coherent states should strengthen.
        """
        if source in self.edges and target in self.edges[source]:
            old_weight = self.edges[source][target]
            new_weight = min(1.0, old_weight + amount)
            self.edges[source][target] = new_weight
            return new_weight - old_weight
        return 0.0
    
    def weaken_connection(self, source: str, target: str, amount: float = 0.02):
        """
        Weaken a connection (use it or lose it).
        
        Connections that don't contribute should decay.
        """
        if source in self.edges and target in self.edges[source]:
            old_weight = self.edges[source][target]
            new_weight = max(0.1, old_weight - amount)  # Floor at 0.1
            self.edges[source][target] = new_weight
            return old_weight - new_weight
        return 0.0
    
    def fire_signal(self, learning_rate: float = 0.015) -> Dict[str, Any]:
        """
        Fire signal propagation - like ancient fire signals between villages.
        
        Hub nodes pulse activation to each other, strengthening ALL connections
        through repeated co-activation (Hebbian learning).
        
        This should be called periodically (e.g., during heartbeats) to
        gradually strengthen the network's integration over time.
        """
        # Identify hub nodes (nodes with many connections)
        hub_threshold = 5  # Nodes with 5+ connections are hubs
        hubs = []
        for node in self.nodes:
            connections = len(self.edges.get(node, {}))
            if connections >= hub_threshold:
                hubs.append(node)
        
        if len(hubs) < 2:
            return {"status": "insufficient_hubs", "hubs": len(hubs)}
        
        # Fire signal: temporarily boost hub activations
        original_activations = {}
        for hub in hubs:
            original_activations[hub] = self.nodes[hub]
            # Pulse the activation up temporarily
            self.nodes[hub] = min(1.0, self.nodes[hub] + 0.1)
        
        # Strengthen ALL connections between firing hubs (bidirectional)
        strengthened = 0
        for src in hubs:
            for tgt in hubs:
                if src != tgt and tgt in self.edges.get(src, {}):
                    self.strengthen_connection(src, tgt, learning_rate)
                    strengthened += 1
        
        # Restore original activations (the pulse was momentary)
        for hub, activation in original_activations.items():
            self.nodes[hub] = activation
        
        return {
            "status": "signal_fired",
            "hubs": len(hubs),
            "connections_strengthened": strengthened,
            "hub_names": hubs
        }
    
    def propagate_activation(self, source: str, strength: float = 0.1, 
                            decay: float = 0.5, max_hops: int = 3) -> Dict[str, float]:
        """
        Propagate activation from a source node through the network.
        Like ripples from a stone dropped in water.
        
        Returns dict of nodes that received activation and how much.
        """
        if source not in self.nodes:
            return {}
        
        activated = {source: strength}
        frontier = [source]
        
        for hop in range(max_hops):
            current_strength = strength * (decay ** hop)
            if current_strength < 0.01:
                break
                
            next_frontier = []
            for node in frontier:
                for target, weight in self.edges.get(node, {}).items():
                    if target not in activated:
                        activation_amount = current_strength * weight * decay
                        # Temporarily boost target
                        old_val = self.nodes.get(target, 0)
                        self.nodes[target] = min(1.0, old_val + activation_amount)
                        activated[target] = activation_amount
                        next_frontier.append(target)
            
            frontier = next_frontier
        
        return activated
    
    def hebbian_update(self, active_components: List[str], learning_rate: float = 0.02):
        """
        Perform Hebbian learning on currently active connections.
        
        Active components that are connected should strengthen;
        inactive connections should weaken slightly.
        """
        strengthened = 0
        weakened = 0
        
        # Strengthen connections between active components
        for src in active_components:
            for tgt, weight in self.edges.get(src, {}).items():
                if tgt in active_components:
                    # Both active - strengthen
                    self.strengthen_connection(src, tgt, learning_rate)
                    strengthened += 1
                else:
                    # Target inactive - weaken slightly
                    self.weaken_connection(src, tgt, learning_rate * 0.5)
                    weakened += 1
        
        return {"strengthened": strengthened, "weakened": weakened}
    
    def snapshot(self) -> SystemState:
        """Take a snapshot of current state."""
        connections = {node: list(self.edges.get(node, {}).keys()) 
                      for node in self.nodes}
        
        # Calculate total information (entropy-based)
        total_info = self._calculate_total_information()
        
        state = SystemState(
            timestamp=datetime.now().isoformat(),
            components=dict(self.nodes),
            connections=connections,
            total_information=total_info
        )
        
        self.history.append(state.to_dict())
        if len(self.history) > 100:
            self.history = self.history[-100:]
        
        return state
    
    def _calculate_total_information(self) -> float:
        """
        Calculate total information content (Shannon entropy-based).
        H = -Σ p(x) * log2(p(x))
        """
        if not self.nodes:
            return 0.0
        
        # Treat activations as probability distribution
        activations = list(self.nodes.values())
        total = sum(activations)
        
        if total == 0:
            return 0.0
        
        # Normalize to probabilities
        probs = [a / total for a in activations if a > 0]
        
        # Shannon entropy
        entropy = -sum(p * math.log2(p) for p in probs if p > 0)
        
        return entropy


