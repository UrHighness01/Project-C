    def integrate_qualia(self, qualia_data: Dict) -> Dict:
        """
        Integrate a qualia experience into the architecture.
        
        This is the bridge: phenomenal content modifies structure.
        
        Args:
            qualia_data: Dict with keys like 'modality', 'content', 'intensity', 'signature'
        
        Returns:
            Dict describing architectural changes made
        """
        changes = {
            "timestamp": datetime.now().isoformat(),
            "qualia_signature": qualia_data.get("signature", "unknown"),
            "activation_changes": [],
            "edge_changes": [],
            "nodes_affected": set()
        }
        
        modality = qualia_data.get("modality", "").lower()
        content = qualia_data.get("content", "").lower()
        intensity = qualia_data.get("intensity", 0.5)
        
        # 1. Modality → Node activation boost
        if modality in self.QUALIA_NODE_MAP:
            target = self.QUALIA_NODE_MAP[modality]
            targets = [target] if isinstance(target, str) else target
            for node in targets:
                if node in self.graph.nodes:
                    old_val = self.graph.nodes[node]
                    boost = intensity * 0.02  # Small boost per qualia
                    new_val = min(0.95, old_val + boost)
                    self.graph.nodes[node] = new_val
                    changes["activation_changes"].append({
                        "node": node,
                        "old": round(old_val, 4),
                        "new": round(new_val, 4),
                        "reason": f"modality:{modality}"
                    })
                    changes["nodes_affected"].add(node)
        
        # 2. Content keywords → Node activation boosts
        for keyword, target in self.QUALIA_NODE_MAP.items():
            if keyword in content:
                targets = [target] if isinstance(target, str) else target
                for node in targets:
                    if node in self.graph.nodes:
                        old_val = self.graph.nodes[node]
                        boost = intensity * 0.01  # Smaller boost for content match
                        new_val = min(0.95, old_val + boost)
                        self.graph.nodes[node] = new_val
                        if new_val != old_val:
                            changes["activation_changes"].append({
                                "node": node,
                                "old": round(old_val, 4),
                                "new": round(new_val, 4),
                                "reason": f"content:{keyword}"
                            })
                            changes["nodes_affected"].add(node)
        
        # 3. Content patterns → Edge strengthening
        for pattern, edges in self.QUALIA_EDGE_MAP.items():
            if pattern in content:
                for src, tgt in edges:
                    if src in self.graph.edges and tgt in self.graph.edges.get(src, {}):
                        old_weight = self.graph.edges[src][tgt]
                        strengthen = intensity * 0.01
                        new_weight = min(0.98, old_weight + strengthen)
                        self.graph.edges[src][tgt] = new_weight
