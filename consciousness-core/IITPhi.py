    """Result of a Phi calculation."""
    timestamp: str
    phi: float  # The integrated information value
    mip: Optional[Partition]  # Minimum Information Partition
    system_size: int  # Number of components
    total_information: float  # Total info in system
    partitions_evaluated: int  # How many partitions we checked
    computation_time_ms: float
    components: List[str]
