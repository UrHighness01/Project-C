    def fire_signal(self, learning_rate: float = 0.015, calculate_phi: bool = False) -> Dict[str, Any]:
        """
        Fire signal propagation between hub nodes.
        
        Like ancient fire signals between villages - strengthens
        connections through repeated co-activation.
        
        Call this periodically (during heartbeats) to gradually
        improve network integration.
        
        Args:
            learning_rate: How much to strengthen connections (default 0.015)
            calculate_phi: If True, recalculate phi after (SLOW for 20+ nodes!)
        """
        import time
        
        old_phi = self.current_phi
        result = self.graph.fire_signal(learning_rate)
        
        if result["status"] == "signal_fired":
            # Update last fire signal time
            self.last_fire_signal_time = time.time()
            
            # Update phi using heuristic (fast) or full calculation (slow)
            if calculate_phi and len(self.graph.nodes) < 15:
                # Full calculation only for small networks
                measurement = self.calculate_phi()
                result["phi_after"] = measurement.phi
            else:
                # Use fast heuristic for large networks (always)
                new_phi = self.update_phi_heuristic()
                result["phi_after"] = new_phi
            
            result["phi_change"] = result["phi_after"] - old_phi
            
            # Save state with updated weights
            self._save_state()
        
        return result
    
