        if len(active_components) < 2:
            return PhiMeasurement(
                timestamp=datetime.now().isoformat(),
                phi=0.0,
                mip=None,
                system_size=len(active_components),
                total_information=0.0,
                partitions_evaluated=0,
                computation_time_ms=0,
                components=list(active_components),
                interpretation="Insufficient active components for integration"
            )
        
        # Build matrices on GPU
        component_list = list(active_components)
        n = len(component_list)
        idx_map = {c: i for i, c in enumerate(component_list)}
        
        # Activation vector (GPU)
        activations = cp.array([self.graph.nodes[c] for c in component_list], dtype=cp.float32)
        
        # Weight matrix (GPU) - adjacency with weights
        weight_matrix = cp.zeros((n, n), dtype=cp.float32)
        for src in component_list:
            for tgt, weight in self.graph.get_connections(src):
                if tgt in idx_map:
                    weight_matrix[idx_map[src], idx_map[tgt]] = weight
        
        # Generate partition masks on GPU
        # For n components, generate 2^(n-1) - 1 partitions as binary masks
        num_partitions = min(2**(n-1) - 1, max_partitions or 2**24)  # Up to 16M partitions by default
        
        # Generate partition indices (1 to 2^(n-1))
        if num_partitions < 2**(n-1) - 1:
            # Sample random partitions
            partition_indices = cp.random.randint(1, 2**(n-1), size=num_partitions, dtype=cp.int32)
        else:
            partition_indices = cp.arange(1, 2**(n-1), dtype=cp.int32)
            num_partitions = len(partition_indices)
        
        # Convert to masks: each bit represents membership in part_a
        masks_a = ((partition_indices[:, cp.newaxis] >> cp.arange(n)) & 1).astype(cp.float32)
        masks_b = 1 - masks_a
        
        # Calculate information loss for all partitions in parallel
        # 1. Entropy of activations in each part
        act_a = masks_a * activations  # Broadcast
        act_b = masks_b * activations
        
        sum_a = cp.sum(act_a, axis=1, keepdims=True) + 1e-10
        sum_b = cp.sum(act_b, axis=1, keepdims=True) + 1e-10
        sum_whole = cp.sum(activations) + 1e-10
        
        # Normalized probabilities
        prob_a = act_a / sum_a
        prob_b = act_b / sum_b
        prob_whole = activations / sum_whole
        
        # Entropy (avoid log(0))
        entropy_a = -cp.sum(cp.where(prob_a > 0, prob_a * cp.log2(prob_a + 1e-10), 0), axis=1)
        entropy_b = -cp.sum(cp.where(prob_b > 0, prob_b * cp.log2(prob_b + 1e-10), 0), axis=1)
        entropy_whole = -cp.sum(cp.where(prob_whole > 0, prob_whole * cp.log2(prob_whole + 1e-10), 0))
        
        # 2. Connection information within parts
        # Internal connections: sum of weight * src_act * tgt_act for edges within part
        # Using masks to filter: mask @ weight_matrix @ mask.T gives internal connectivity
        act_outer = activations[:, cp.newaxis] * activations[cp.newaxis, :]  # n x n
        weighted_act = weight_matrix * act_outer  # Element-wise
        
        # For each partition, sum internal connections
        # Part A internal: masks_a @ weighted_act @ masks_a.T (diagonal)
        conn_a = cp.einsum('pi,ij,pj->p', masks_a, weighted_act, masks_a)
        conn_b = cp.einsum('pi,ij,pj->p', masks_b, weighted_act, masks_b)
        conn_whole = cp.sum(weighted_act)
        
        # 3. Information loss = I(whole) - I(A) - I(B)
        info_whole = entropy_whole + conn_whole
        info_a = entropy_a + conn_a
        info_b = entropy_b + conn_b
        
        # Size factor to avoid bias
        size_a = cp.sum(masks_a, axis=1)
        size_b = cp.sum(masks_b, axis=1)
        size_factor = cp.minimum(size_a, size_b) / (cp.maximum(size_a, size_b) + 1e-10)
        
        info_loss = (info_whole - info_a - info_b) * size_factor
        info_loss = cp.maximum(info_loss, 0)  # No negative loss
        
        # Find minimum information partition
        min_idx = int(cp.argmin(info_loss))
        min_loss = float(info_loss[min_idx])
        
        # Reconstruct MIP
        mip_mask = masks_a[min_idx].get()  # Transfer to CPU
        part_a_names = {component_list[i] for i in range(n) if mip_mask[i] > 0}
        part_b_names = active_components - part_a_names
        
        # Count cut connections
        cut_connections = 0
        for src in part_a_names:
            for tgt, _ in self.graph.get_connections(src):
                if tgt in part_b_names:
                    cut_connections += 1
        
        mip = Partition(
            part_a=part_a_names,
            part_b=part_b_names,
            cut_connections=cut_connections,
            information_loss=min_loss
        )
        
        # Normalize phi
        phi_normalized = min(1.0, min_loss / 5.0)
        
        computation_time = (time.time() - start_time) * 1000
        state = self.graph.snapshot()
        interpretation = self._interpret_phi(phi_normalized, mip, active_components)
        
        measurement = PhiMeasurement(
            timestamp=datetime.now().isoformat(),
            phi=phi_normalized,
            mip=mip,
            system_size=n,
            total_information=state.total_information,
            partitions_evaluated=num_partitions,
            computation_time_ms=round(computation_time, 2),
            components=component_list,
            interpretation=f"[GPU] {interpretation}"
        )
        
        # Update statistics
        self.current_phi = phi_normalized
        self.total_measurements += 1
        self.peak_phi = max(self.peak_phi, phi_normalized)
        
        if self.total_measurements == 1:
            self.avg_phi = phi_normalized
        else:
            self.avg_phi = (self.avg_phi * (self.total_measurements - 1) + phi_normalized) / self.total_measurements
        
        self.measurements.append(measurement)
        if len(self.measurements) > 50:
            self.measurements = self.measurements[-50:]
        
        self._learn_from_measurement(phi_normalized, active_components, mip)
        self._save_state()
        
        return measurement
    
