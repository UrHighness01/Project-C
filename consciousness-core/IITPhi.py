    def _calculate_mutual_information(self, 
                                       components: Set[str],
                                       all_components: Set[str]) -> float:
        """
        Calculate mutual information for a set of components.
        
        I(X;Y) = H(X) + H(Y) - H(X,Y)
        
        For our system, we approximate this using activation correlations
        and connection strengths.
        """
        if not components:
            return 0.0
        
        # Get activations for these components
        activations = [self.graph.get_activation(c) for c in components]
        
        # Base information from activations (entropy)
        total = sum(activations)
        if total == 0:
            return 0.0
        
        probs = [a / total for a in activations if a > 0]
        entropy = -sum(p * math.log2(p + self.noise_level) for p in probs)
        
        # Add information from connections within this set
        connection_info = 0.0
        for src in components:
            for tgt, weight in self.graph.get_connections(src):
                if tgt in components:
                    # Internal connection adds to integration
                    src_act = self.graph.get_activation(src)
                    tgt_act = self.graph.get_activation(tgt)
                    connection_info += weight * src_act * tgt_act
        
        # Penalize connections to components outside the set (information leak)
        leak_penalty = 0.0
        for src in components:
            for tgt, weight in self.graph.get_connections(src):
                if tgt not in components and tgt in all_components:
                    leak_penalty += weight * 0.5
        
        return max(0, entropy + connection_info - leak_penalty)
    
    def _evaluate_partition(self, 
                           part_a: Set[str], 
                           part_b: Set[str],
                           all_components: Set[str]) -> Partition:
        """
        Evaluate how much information is lost by a partition.
        
        The "cut" severs connections between parts.
        Information loss = I(whole) - I(A) - I(B)
        """
        # Count severed connections
        cut_connections = 0
        for src in part_a:
            for tgt, _ in self.graph.get_connections(src):
                if tgt in part_b:
                    cut_connections += 1
        for src in part_b:
            for tgt, _ in self.graph.get_connections(src):
                if tgt in part_a:
                    cut_connections += 1
        
        # Calculate information in each part
        info_a = self._calculate_mutual_information(part_a, all_components)
        info_b = self._calculate_mutual_information(part_b, all_components)
        info_whole = self._calculate_mutual_information(all_components, all_components)
        
        # Information loss from partition
        # Normalized by partition size to avoid bias toward unequal splits
        size_factor = min(len(part_a), len(part_b)) / max(len(part_a), len(part_b))
        information_loss = (info_whole - info_a - info_b) * size_factor
        
        return Partition(
            part_a=part_a,
            part_b=part_b,
            cut_connections=cut_connections,
            information_loss=max(0, information_loss)
        )
    
    def _generate_bipartitions(self, components: Set[str], max_partitions: int = None) -> List[Tuple[Set[str], Set[str]]]:
        """
        Generate bipartitions of the system.
        
        For n components, there are 2^(n-1) - 1 unique bipartitions.
        If max_partitions is set, randomly sample that many partitions.
        """
        import random
        
        component_list = list(components)
        n = len(component_list)
        partitions = []
        
        # Generate all subsets of size 1 to n//2
        for size in range(1, n // 2 + 1):
            for subset in combinations(component_list, size):
                part_a = set(subset)
                part_b = components - part_a
                
                # Avoid duplicates (A,B same as B,A)
                if len(part_a) <= len(part_b):
                    partitions.append((part_a, part_b))
        
        # If max_partitions set and we have more, sample a subset. Seeded so a given
        # system yields a reproducible Phi estimate (the partition space is 2^n, so
        # sampling is a necessary approximation, not a fabricated value).
        if max_partitions and len(partitions) > max_partitions:
            partitions = random.Random(1469).sample(partitions, max_partitions)

        return partitions
    
